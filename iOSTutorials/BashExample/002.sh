#!/bin/bash

# 传递参数

echo "=============Shell 传递参数示例！============="
echo "执行的脚本文件名：$0"
echo "参数个数：$#"
echo "第一个参数：$1"
echo "第二个参数：$2"
echo "传递的参数作为字符串显示：$*"
echo "传递的参数作为字符串显示：\"$*\""

echo "===== 演示 \$* 的效果======"
for i in "$*"; do
    echo "\"$i\""
done

echo "===== 演示 \$@ 的效果======"
for i in "$@"; do
    echo "\"$i\""
done

echo "当前进程 ID 号：$$"
echo "后台运行的最后一个进程 ID 号：$!"
echo "最后命令的退出状态：$?"